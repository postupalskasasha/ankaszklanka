let px = [];
let py = [];
let z = 0;
let s = 8;

function setup() { 
createCanvas(1920, 1080); 
}

function draw() {
  
  background(255); 
  noFill();
  textSize(15);      // Ustaw rozmiar tekstu
  text("start here", 80, 870);  // Napisz tekst w punkcie (80, 870)
  text("connect the dots", 60, 970);  // Napisz tekst w punkcie (80, 870)
 for (let i = 0; i < points.length; i++) { 
  let x = points[i][0]; 
  let y = points[i][1]; 
  ellipse(x, y, 1, 1);
text(i + 1, x + 5, y);
 }
beginShape();
for(let i = 0; i < px.length; i++){ 
    vertex(px[i],py[i]);
  }
  
  endShape();
  
  stroke(0);
  strokeWeight(1);
  
  for(let i = 0; i < px.length; i++){ 
    point(px[i],py[i]);
  }
}

function mouseReleased(){  

  if(mouseX > points[z][0]-s && mouseX < points[z][0] + s 
  && mouseY > points[z][1]-s && mouseY < points[z][1]+s) {
   z++;
  px.push(mouseX);
  py.push(mouseY);
  }
  
} 
